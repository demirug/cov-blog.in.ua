        <p>In the second book, Harry Potter and the Chamber of Secrets, Rowling pits Harry against Tom Riddle, Lord Voldemort's "memory" within a secret diary which has possessed Ron's younger sister Ginny. When Muggle-born students are suddenly being Petrified, many suspect that Harry may be behind the attacks, further alienating him from his peers. Furthermore, Harry begins to doubt his worthiness for House of Gryffindor, particularly considering he discovers he shares Lord Voldemort's ability to communicate with snakes via Parseltongue. In the climax, Ginny disappears. To rescue her, Harry battles Riddle and the monster he controls that is hidden in the Chamber of Secrets. To defeat the monster, Harry summons the Sword of Godric Gryffindor from the Sorting Hat supplied by Dumbledore's pet phoenix, Fawkes. In doing so, Dumbledore later restores Harry's self-esteem by explaining that that feat is clear proof of his worthiness of his present house.</p>
