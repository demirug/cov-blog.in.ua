        <p>Harry first appears in Harry Potter and the Philosopher's Stone. Starting in 1981, when Harry was just one year old, his parents, James and Lily, were murdered by the most powerful Dark Wizard, Lord Voldemort (subsequently called "You-Know-Who" and "He-Who-Must-Not-Be-Named" by those too superstitious to use his actual name). He attempted to kill Harry too, but was unsuccessful and only left a lightning bolt shaped scar on Harry's forehead. Voldemort's body was destroyed, but his soul was not. Harry later learns that the reason why he survived was that his mother sacrificed herself for him, and her love was something that Voldemort could not destroy.</p>
        <p>According to Rowling, fleshing out this back story was a matter of reverse planning: "The basic idea [is that] Harry ... didn't know he was a wizard ... and so then I kind of worked backwards from that position to find out how that could be, that he wouldn't know what he was... That's... When he was one year old, the most evil wizard for hundreds and hundreds of years attempted to kill him. He killed Harry's parents, and then he tried to kill Harryâ€”he tried to curse him.... Harry has to find out, before we find out. And for some mysterious reason, the curse didn't work on Harry. So he's left with this lightning-bolt shaped scar on his forehead, and the curse rebounded upon the evil wizard who has been in hiding ever since".</p>
