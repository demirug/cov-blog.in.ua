        <p>In the third book, Harry Potter and the Prisoner of Azkaban, Rowling uses a time travel premise. Harry learns that his parents were betrayed to Voldemort by their friend Peter Pettigrew, who framed Harry's godfather Sirius Black for the crimes, condemning him to Azkaban, the wizard prison. When Sirius escapes to find Harry, Harry and Hermione use a Time Turner to save him and a hippogriff named Buckbeak. When Pettigrew escapes, an innocent Sirius becomes a hunted fugitive once again. Harry learns how to create a Patronus, which takes the form of a stag, the same as his late father's.</p>
